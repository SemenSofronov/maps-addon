package com.haulmont.addon.maps.web.components;

public class AreasSettings {

    protected String unlistedAreasColor = "#DDDDDD";
    protected Double unlistedAreasAlpha = 1.;

    public String getUnlistedAreasColor() {
        return unlistedAreasColor;
    }

    public void setUnlistedAreasColor(String unlistedAreasColor) {
        this.unlistedAreasColor = unlistedAreasColor;
    }

    public Double getUnlistedAreasAlpha() {
        return unlistedAreasAlpha;
    }

    public void setUnlistedAreasAlpha(Double unlistedAreasAlpha) {
        this.unlistedAreasAlpha = unlistedAreasAlpha;
    }
}
